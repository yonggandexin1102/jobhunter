package usermanager.entity;

public class UserEduInfo {

	private int edu_id;
	private String user_school_name;
	private String user_major_name;
	private String user_diploma;
	private String user_graduate_time;
	private int user_id;

	public int getEdu_id() {
		return edu_id;
	}

	public void setEdu_id(int edu_id) {
		this.edu_id = edu_id;
	}

	public String getUser_school_name() {
		return user_school_name;
	}

	public void setUser_school_name(String user_school_name) {
		this.user_school_name = user_school_name;
	}

	public String getUser_major_name() {
		return user_major_name;
	}

	public void setUser_major_name(String user_major_name) {
		this.user_major_name = user_major_name;
	}

	public String getUser_diploma() {
		return user_diploma;
	}

	public void setUser_diploma(String user_diploma) {
		this.user_diploma = user_diploma;
	}

	public String getUser_graduate_time() {
		return user_graduate_time;
	}

	public void setUser_graduate_time(String user_graduate_time) {
		this.user_graduate_time = user_graduate_time;
	}
	

	public int getUser_id() {
		return user_id;
	}

	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}

}
